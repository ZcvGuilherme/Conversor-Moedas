package elements;

public class Coin {
	private String code, codein, name, high, low, varBid, pctChange, bid, ask, timestamp, create_date;
    
    public Coin() {
		
	}

	public String getCode() {
		return code;
	}

	public String getCodein() {
		return codein;
	}

	public String getName() {
		return name;
	}

	public String getHigh() {
		return high;
	}

	public String getLow() {
		return low;
	}

	public String getVarBid() {
		return varBid;
	}

	public String getPctChange() {
		return pctChange;
	}

	public String getBid() {
		return bid;
	}

	public String getAsk() {
		return ask;
	}

	public String getTimestamp() {
		return timestamp;
	}

	public String getCreate_date() {
		return create_date;
	}

	
   
}
